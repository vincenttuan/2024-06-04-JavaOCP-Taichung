package day05;

import java.util.Arrays;

public class MultiArray1 {

	public static void main(String[] args) {
		// 建立一個 3x3 的二維陣列
		char[][] x = new char[3][3];
		x[0][0] = 'O';
		System.out.println(Arrays.toString(x[0]));
		System.out.println(Arrays.toString(x[1]));
		System.out.println(Arrays.toString(x[2]));
		

	}

}
