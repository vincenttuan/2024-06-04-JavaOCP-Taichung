package day04;

import java.util.Arrays;

public class SimpleBubbleSort3 {

	public static void main(String[] args) {
		// 泡沫排序法(大到小)
		int[] scores = {10, 8, 6, 7, 5};
		// 排序前
		System.out.println("排序前:" + Arrays.toString(scores));
		
		// 排序後
		System.out.println("排序後:" + Arrays.toString(scores));
		
	}

}
